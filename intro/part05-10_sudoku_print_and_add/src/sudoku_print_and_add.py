def print_sudoku(sudoku: list):
    i, j= 0, 0
    for row in sudoku:
        j += 1
        for item in row:
            i += 1
            if item == 0:
                item = "_"
            print(f"{item}", end = " ")
            if i%3 == 0:
                print(" ", end = "")
        print()
        if j%3 == 0:
            print()

def add_number(sudoku: list, row_no: int, column_no: int, number:int):
    sudoku[row_no][column_no] = number

